//! TypeScript 类型生成器
//!
//! 使用 specta 从 Rust 类型生成 TypeScript 接口定义。
//! 运行：`cargo run --bin generate_types > dashboard-ui/src/generated/types.ts`

use specta::ts::inline;
use specta::ts::ExportConfiguration;
use xun::xun_core::operation::{Change, OperationResult, Preview, RiskLevel};
use xun::xun_core::value::{ColumnDef, Table, ValueKind};
use xun::xun_core::ws_protocol::{WsCommand, WsErrorCode, WsResponse};

fn main() {
    let conf = ExportConfiguration::default();

    println!("// ============================================================");
    println!("// Auto-generated by `cargo run --bin generate_types`");
    println!("// Do NOT edit manually — regenerate with the command above.");
    println!("// ============================================================\n");

    // Value 类型（用 interface 打破循环引用）
    println!("/** XunYu 统一结构化值 */");
    println!("export interface ValueArray extends Array<Value> {{}}");
    println!("export interface ValueRecord {{ [key: string]: Value; }}");
    println!("export type Value = null | boolean | number | string | ValueArray | ValueRecord;\n");

    // ValueKind 枚举
    println!("/** 列的语义类型 */");
    println!(
        "export type ValueKind = {};\n",
        inline::<ValueKind>(&conf).unwrap()
    );

    // ColumnDef
    println!("/** 列定义 */");
    println!(
        "export type ColumnDef = {};\n",
        inline::<ColumnDef>(&conf).unwrap()
    );

    // Table
    println!("/** 带 schema 的表格 */");
    println!(
        "export type Table = {};\n",
        inline::<Table>(&conf).unwrap()
    );

    // RiskLevel
    println!("/** 风险等级 */");
    println!(
        "export type RiskLevel = {};\n",
        inline::<RiskLevel>(&conf).unwrap()
    );

    // Change
    println!("/** 变更描述 */");
    println!(
        "export type Change = {};\n",
        inline::<Change>(&conf).unwrap()
    );

    // Preview
    println!("/** 操作预览 */");
    println!(
        "export type Preview = {};\n",
        inline::<Preview>(&conf).unwrap()
    );

    // OperationResult
    println!("/** 操作执行结果 */");
    println!(
        "export type OperationResult = {};\n",
        inline::<OperationResult>(&conf).unwrap()
    );

    // WebSocket 协议类型
    println!("/** WebSocket 错误码 */");
    println!(
        "export type WsErrorCode = {};\n",
        inline::<WsErrorCode>(&conf).unwrap()
    );

    println!("/** WebSocket 命令 */");
    println!(
        "export type WsCommand = {};\n",
        inline::<WsCommand>(&conf).unwrap()
    );

    println!("/** WebSocket 响应 */");
    println!(
        "export type WsResponse = {};\n",
        inline::<WsResponse>(&conf).unwrap()
    );
}
